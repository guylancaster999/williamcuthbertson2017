<body><p align="center"><strong>威廉</strong><strong>·</strong><strong>卡思伯森</strong><strong> </strong></p>
<p>• <strong>个人介</strong><strong>绍</strong><strong>和</strong><strong>经历</strong><strong>：</strong></p>
<p>生于英格兰的塞文欧克斯（Sevenoaks），于中学时期接受Bernd King大师的指导。威廉卡思伯森专精于萧邦作品的演奏，于童年时就已得到无数的钢琴演奏大奖。1975-1979年间于位于露丝·哈特和戈登·格林的皇家音乐学院完成音乐教育。 </p>
<p>由于对于萧邦作品的喜好而前往波兰，于当地接受Ludwik Stefanski大师的指导，并接受波兰政府的奖学金的补助。在他的音乐教育的经历中Peter Feuchtwanger大师扮演着一个很重要的角色。 </p>
<p>目前威廉·卡思伯森是自由钢琴演奏家，定居于德国Waldkirch，专精于钢琴演奏会和钢琴课程的授予。威廉·卡思伯森是即兴演奏的大师，他的音乐常出现于无声电影中，为电影作更自由即兴的诠释。 </p>
<p>威廉·卡思伯森目前已于德国，荷兰，波兰，丹麦，挪威，英国，奥地利，瑞典，瑞士和美国举办个人的演奏会。并于2010年在Waldkirch各个不同演奏厅所举​​办庆祝萧邦200年诞辰的一系列6场连环演奏会，及2011年3场庆祝李斯特200年诞辰演奏会中演出。 </p>
<p>&nbsp;</p>
<p>• <strong>教学，</strong><strong>钢</strong><strong>琴售</strong><strong>课</strong><strong>，和伴奏</strong></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;威廉·卡思伯森拥有对于钢琴课了广泛的授课经验，并熟悉适合各年龄段的学生 <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;的教学方法。 <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在钢琴授课上威廉·卡思伯森擅长教授各种不同派别和风格，尤其对Peter<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Feuchtwagerns  的风格有独到的见解。 </p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;目前提供的训练课程，钢琴课程和伴奏包括： </p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;－ 初级训练，教练专业钢琴家 <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;－ 音乐伴奏和指导其他乐器 <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;－ 凭借其精细的方法论知识，他针对学生的个性化需求，指导和辅助他们在音乐 <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;上发展的个人走向。 <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;－ 教导自然和个人化的钢琴演奏技巧– 目前担任Peter Feuchtwanger教授的钢琴 <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;练习课程的指导老师。 <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;－ 通过对音乐作品内涵，结构，和对听者的影响进行分析，协助学生在作品的诠 <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;释上创造自己的风格，以创造动人的音乐。 </p>
