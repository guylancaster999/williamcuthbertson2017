						The Georgian pianist Ekaterine Khvedelidze began playing the piano at the age of five. 
						In 1996, her extraordinary artistic development led her to having lessons with the Georgian piano teacher Medea Gamsakhurdia, with whom she studied at the music college in her hometown. 
						After completing her diploma, she continued her studies at the conservatories in Detmold and Moscow.
						<br/> <br/> 
						Since 2004, she has been a master pupil of her current mentor and sponsor Igor Zhukov,one of the most important representatives of the older generation of Russian pianists. 
						Zhukov was a pupil of the legendary Heinrich Neuhaus and is the last remaining personal representative of his exemplary teaching. 
						In addition, Ekaterine Khvedelidze attended the master classes of various internationally renowned musical personalities, such as Vitaly Margulis, Peter Feuchtwanger, Andras Schiff Bruno Canino and John Perry.
						<br/> <br/> 
						Ekaterine Khvedelidze has impressed her audiences for years with her incomparable way of penetrating deeply into the soul of the music, giving every phrase a natural breath. 
						In her animated, colorful and imaginative playing, the bare musical text, the instrument and her natural virtuosity retreat into the background, allowing an experience of the deeper layers of musical experience.
