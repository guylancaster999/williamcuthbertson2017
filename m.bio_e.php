					 Pianist <b>William Cuthbertson</b>, who was born in Sevenoaks, England, has lived in Waldkirch in the German Black Forest, for many years. He plays an active part in local cultural activities, both as performer and organiser. 
<br/><br/>
As a child, Cuthbertson won many musical prizes in his home county of Kent as a pupil of Bernard King at <a href="https://www.sevenoaksschool.org">Sevenoaks School</a>. During this time he had additional lessons with <a href="http://www.bach-cantatas.com/Lib/Langrish-Vivian.htm">Vivian Langrish</a>. 
<br/><br/>
He studied piano at the <a href="http://www.ram.ac.uk/" title="Royal Academy of Music">Royal Academy of Music</a> in London with Ruth Harte and Gordon Green. He then continued his studies at the <a href="https://www.amuz.krakow.pl/">Akadamia Muzyczna, Krakow, Poland</a> on a Polish Government scholarship, studying with Ludwik Stefanski, and has continued to specialise in playing the works of Chopin. 
<br/>
<br/>
A very significant person in William Cuthbertson’s life was the renowned London-based German pianist, composer and pedagogue, <a href="http://www.peter-feuchtwanger.de/">Peter Feuchtwanger</a>.
From 1999 William Cuthbertson organised an annual International Master Class with  Peter Feuchtwanger in Waldkirch. 
Since Feuchtwanger´s death in 2016, the Master Class continues to take place with Cuthbertson - click <a href="m.meisterkurse_e.php">here</a> for details. 
<br/>
<br/>
Cuthbertson is considered a master of improvisation, a skill he developed at Sevenoaks School under the guidance of Brian Townend and from time to time he accompanies silent films with free improvisation at the local community cinema, <a href="http://www.klappe11.de/">Klappe 11</a>. His teaching is eclectic, using what approach is most applicable, complimantary and at the same time in harmony with Peter Feuchtwanger's brilliant insights. 
<br/><br/>
William Cuthbertson has given concerts in Germany, Holland, Poland, Denmark, Norway, England, Russia, Austria, Sweden, Switzerland and the United States.  In 2010 , Chopin’s 200<sup>th</sup> anniversary, he gave a series of six recitals in Waldkirch and in 2011, three Liszt recitals for Liszt's 200<sup>th</sup> anniversary. 
<br/><br/>
In November 2017 he will be serving on the jury of the 4<sup>th</sup> Thailand  International Chopin Competition in Bangkok. 
