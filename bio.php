<b>William Cuthbertson</b>, geboren in Sevenoaks, England , wohnt seit vielen Jahren im Waldkirch im Schwarzwald. 
Während seiner Schulzeit in <a href="https://www.sevenoaksschool.org">Sevenoaks  School</a> wurde er von Bernard King unterrichtet. Zusätzliche  Unterricht nahm er bei <a href="http://www.bach-cantatas.com/Lib/Langrish-Vivian.htm">Vivian Langrish</a>.  
Der Chopin-Spezialist gewann bereits als Kind viele Klavierpreise. 
Sein Studium absolvierte er von 1975 – 1979 an der <a href="http://www.ram.ac.uk/" title="Royal Academy of Music">Royal Academy of Music</a> in London bei Ruth Harte und Gordon Green. 
Die Neigung zu Chopin führte ihn nach Polen, wo er in   <a href="https://www.amuz.krakow.pl/">Krakau</a> 2 Jahre lang bei Ludwik Stefanski mit einem Stipendium der polnischen Regierung studierte. 
<br/>
<br/>
Eine Schlüsselfigur seines musikalischen Werdeganges stellt jedoch  <a href="http://www.peter-feuchtwanger.de/">Peter Feuchtwanger</a> dar. William Cuthbertson ist freiberuflich als Konzertpianist und Klavierlehrer tätig und lebt in Waldkirch. Er gilt als Meister der Improvisation; z.B. begleitet er immer wieder  Stummfilme mit freier Improvisation im kommunales Kino <a href="http://www.klappe11.de/">Klappe11</a>.  Sein Unterricht befasst sich mit verschiedenen Ansätzen die er verwendet je nach Bedarf, ergänzend jedoch im Einklang mit die genialen Einsichten Peter Feuchtwangers. 
<br/>
<br/>
Seit 1999 organisiert Cuthbertson einen <a href="meisterkurse.php">Meisterkurs</a> in Waldkirch mit Feuchtwanger, der 2016 starb. Der Meisterkurs wird noch fortgesetzt unter der Leitung von Cuthbertson.
<br/>
<br/>
Konzerte hat er  in Deutschland, Holland, Polen, Dänemark, Norwegen, England, Russland, Österreich, Schweden, der Schweiz und in den USA gegeben. 2010 spielte er in Waldkirch einen  Wanderzyklus mit 6 Konzerten zum 200. Jubiläum von Frédéric Chopin und 2011 3 Konzerte zum 200. Jubiläum von Franz Liszt
<br/>
<br/>
In November 2017 wird er Mitglied des Jurys beim 4. Thailand Internationalen Chopin Wettbewerbes in Bangkok.
