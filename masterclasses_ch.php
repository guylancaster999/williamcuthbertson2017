格瓦尔日德钢琴演奏家赫维德利泽（Katerine Khvedelidze）五岁开始弹钢琴。 1996年，她非凡的艺术发展带领她与格鲁吉亚钢琴老师美加娅·甘萨克古迪亚（Medea Gamsakhurdia）接触，并与她在家乡音乐学院学习。完成文凭后，她继续在Detmold和莫斯科的音乐学院学习。
<br/><br/>



自2004年以来，她一直是她当前的导师的主修学生，并赞助了伊戈尔·朱可夫，这是俄罗斯老一代钢琴家最重要的代表人物之一。朱可夫是传说中的海因里希·诺伊豪斯（Heinrich Neuhaus）的学生，也是他的典范教学的最后一位个人代表。除此之外，叶卡捷琳娜还参加了各种国际知名音乐人物的大师班，如维塔利·玛格丽丝，彼得·费赫特旺格，安德拉斯·席夫·布鲁诺·卡诺诺和约翰·佩里。
<br/><br/>
叶卡捷琳娜·赫维德利泽以她无与伦比的渗透到音乐灵魂中的方式给观众留下了深刻的印象，让每一句话都自然而然。在她的动画，多彩和富有想象力的演奏中，裸露的音乐文字，乐器和她的自然精湛技巧退缩到背景中，让您体验更深层次的音乐体验。
<br/><br/>
她对晚期浪漫的俄罗斯作曲家Alexander Scriabin和Nikolai Medtner的作品特别感兴趣。她的演奏既强大又敏感，他们的不公正的忽视和鲜为人知的作品在她激动人心的钢琴独奏中一次又一次地带来了惊喜。
