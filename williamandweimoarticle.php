<h2>Press Review </h2>
<a href="http://www.badische-zeitung.de/waldkirch/taenzerische-leichtigkeit-und-elegische-momente--80093094.php"
 
  title="William Cuthbertson+Weimo Gao:Photo Hildegard Karig, Badische-zeitung"><img class="picture" src="img/williamandweimo_240.jpg" class="pictureright"  width="25%" alt="William Cuthbertson+Weimo Gao:Photo Hildegard Karig, Badische-zeitung"/></A>
<h3>NeueJahre Concert 2014</h3>
<p>Badische Zeitung 28/1/14</p>
<p> <strong>Dance-like ease and  elegiac moments.</strong><br />
  <strong>William Cuthbertson and Weimo Gau perform  old master Bach - and also a Chinese lulaby.</strong>
 <br/><br/> 
WALDKIRCH. The New Year's Concert of the Waldkirch Organ Foundation on Sunday morning with the Waldkirch pianist, William Cuthbertson and the young Chinese cellist, Weimo Gao from Shanghai was for visitors, a successful start of this year's events in  the &ldquo;Orgelbauersaal&rdquo; (organ builders hall). The mixture of solo and duo performances comprised a contrast of  well-known and completely unknown works.
<br />
<br />
The program began with the Solo Suite No. 3 for cello by Johann Sebastian Bach.  His solo suites were regarded for  a long  time as teaching pieces and it was due to the legendary interpretations by  Pablo Casals that they came to be included in the concert programs of almost  every cellist. It was brave of Weimo Gao to play  the Suite No. 3, in which there is only her,  her playing and Bach's music, at the very beginning, 
<br />
<br />
The young cellist has been studying for four years with Christoph Henkel, a  professor at the Musikhochschule in Freiburg. She rose to the challenge that  this highly demanding musical monologue every performer ais confronted with and  her playing became more confident and expressive from movement to movement..  She performed the first Bourrée I  with  the dance-like ease which is inherent in this piece and which makes it  such an attractive and well-known encore for  cellists. <br />
  <br />
&ldquo;Causerie&rdquo; or &quot;Chat&quot;, a piece for solo piano by the Russian-French composer, C&eacute;sar Cui, was the first in a series of  salon pieces. William Cuthbertson once again showed himself to be  a versatile pianist, both as a reliable accompanist and as  a soloist, playing  the two Nocturnes op.15 by Chopin after the break.  In the &quot;Romantic  piece&quot; of the Chinese composer Liu Zhuang, the &quot;Lullaby&quot; by Xu  Xiyi  and the &quot;Three small pieces&quot; composed by Cuthbertson specially for Weimo Gao, Cuthbertson and Gao demonstrated their musical togetherness in the small details of a wide variety of moods that were amazingly European oriented. Included in the program were the &quot;Rococo Variations&quot; by  Tchaikovsky, which are for cellists one of the most technically demanding  works in the repertoire. 
<br />
<br />
Extreme maneuverability and ease of playing and at the same time a captivating  singing expression in the quieter variations : Weimo Gao played the elegiac  moments with a beautiful tone and employed a fascinating technical virtuosity  with grest assurance and composure. These variations, originally written for  cello and orchestra, are another example of Tchaikovsky's art of orchestration. Especially in the opening bars the juxtaposition of wind instruments and a  warm cello sound is particularly beautiful. With a piano reduction losses must  always be taken into account, however the way these two artists played resulted in a rewarding encounter with a great work.
<br />
<br />
The two musicians responded to the enthusiastic  applause with an encore &quot; the &quot;Swan&quot;  from the Carnival of the Animals by Camille Saint-Saens. Another opportunity for the cellist to play with mellow, full cello sounds.</p>
