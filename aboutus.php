               TVS was inspired by Archie Sebeko <strong>(</strong>also known as Zola  Zembe). 
            Archie was born in Tyume Valley  and now lives in Tynemouth.
            He distinguished himself in the struggle against  apartheid and was awarded the Order of Luthuli in Silver by President Thabo  Mbeki.
            <blockquote>&nbsp;&nbsp;&nbsp;&ldquo;I was born and bred in the Tyume valley and went to Kwezana  school, which was then based in the church.
            When I was able to go back to South Africa after 1990, I took my wife to  Tyume to see where I had come from and meet my relatives.  
            There was now a school building, but not  much else had changed in over 40 years. 
            The building had mud floors, no ceiling and no water supply. There was  no electricity in the classrooms. There were no books, posters on the walls, art  materials, sports equipment or musical instruments.
            The children were  delightful, well behaved and could sing, but they were still getting &lsquo;Bantu  education&rsquo;, the rote learning suitable for the hewers of wood and drawers of  water that the apartheid regime had intended them to be.<br />
            <br/>
            &nbsp;&nbsp;&nbsp;We felt we ought to do something.
            The parents&rsquo; priority was for water to be  available, so we went into Alice and bought a water butt to replace the one  that had once been there to catch rainwater off the roof but had long since  sprung a leak.<br />
            <br/>
            &nbsp;&nbsp;&nbsp;When we returned to Manchester, friends got interested in  trying to help the school and started to raise money for books, sports kits  etc.  Maggie Kiloh, an old friend from  Zambian days with a great commitment to South Africa, got involved.
            She and  Margaret Woodward went out to see what practical help they could give the  school &ndash; and it all began from there.<br />
