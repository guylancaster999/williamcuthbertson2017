			<h4><a title="Peter Feuchtwanger" href="http://peter-feuchtwanger.de" target = "_blank"> Peter Feuchtwanger,</a> 
			(26.6.1930-18.6.2016)</h4> wurde in München, Deutschland geboren.
			Kompositionsstudium bei 
			<a href="https://de.wikipedia.org/wiki/Paul_M%C3%BCller-Z%C3%BCrich">Paul Müller-Zürich</a>,
			<a href="http://gedenkbuch.univie.ac.at/index.php?id=435&no_cache=1&L=2&person_single_id=26513&person_name=&person_geburtstag_tag=not_selected&person_geburtstag_monat=not_selected&person_geburtstag_jahr=not_selected&person_fakultaet=not_selected&person_kategorie=not_selected&person_volltextsuche=&search_person.x=1&result_page=50" target="_blank">Dr. Hans Heimler</a> 
			(Schüler von <a href="https://de.wikipedia.org/wiki/Alban_Berg"  target = "_blank">Alban Berg</a>, 
			<a href="https://de.wikipedia.org/wiki/Heinrich_Schenker" target = "_blank">Heinrich Schenker</a> und <a href="https://de.wikipedia.org/wiki/Felix_Weingartner" target = "_blank">Felix Weingartner</a>) 
			und <a href="https://de.wikipedia.org/wiki/Lennox_Berkeley" target = "_blank">Lennox Berkeley</a>, in Indischer und Arabischer Musik und Philosophie bei <A href="https://en.wikipedia.org/wiki/Nazir_Jairazbhoy" target = "_blank">Nazir Jairazbhoy</a> und Dr.Arnold Bake, 
			in Klavier bei Gerti Rainer 
			(Schülerin von  <a href="https://de.wikipedia.org/wiki/Emil_von_Sauer_(Komponist)" target = "_blank">Emil von Sauer</a>), 
			Max Egger, <a href="https://de.wikipedia.org/wiki/Edwin_Fischer" target = "_blank">Edwin Fischer</a>
			 und <A href="https://de.wikipedia.org/wiki/Walter_Gieseking" target = "_blank">Walter Gieseking</a>.
			Entscheidender musikalischer Einfluss durch die Begegnung mit <a href="https://de.wikipedia.org/wiki/Clara_Haskil" target = "_blank">Clara Haskil</a>.
			Befasste sich  intensiv mit der Kunst des <A href="https://de.wikipedia.org/wiki/Belcanto" target="_blank">Belcanto</a> im goldenen Zeitalter des Gesanges.
			Jurymitglied vieler internationaler Klavierwettbewerbe und Vizepräsident der  <a 
			href="http://www.epta-uk.org/" target = "_blank">EPTA UK</a>, Professor und Vize-Präsident <A href="http://www.musik-bildung.de/" title="Internationaler Akademie für musikalische Bildung" target = "_blank">Internationaler Akademie für musikalische  Bildung e.V.</a> und war Gast Professor am   <a href="https://www.moz.ac.at" target = "_blank">Mozarteum</a> Salzburg.
			Seit 1967 Meisterkurse in aller Welt.
